    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        int linha = tblProduto.getSelectedRow();
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione um produto", "Erro - Produto não selecionado", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            Produto p = (Produto) tblProduto.getValueAt(linha, 1);
            ProdutoControle.alter(p);
            Atualizar();
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
          
        if(cboCategoria.getSelectedItem().equals("Todos Produtos")){
            if(txtCod.getText().equals("")){
                model = modelProduto.findByName(txtNome.getText());
                tblProduto.setModel(model);
                this.repaint();
            }
            else{
                int cod =Integer.parseInt(txtCod.getText());
                model = modelProduto.findByCod(cod);
                tblProduto.setModel(model);
                this.repaint();
            }
        }
        else{
            if(txtCod.getText().equals("")){
                model = modelProduto.findByNameCategoria(txtNome.getText(), (Categoria) cboCategoria.getSelectedItem());
                tblProduto.setModel(model);
                this.repaint();
            }
            else{
                int cod =Integer.parseInt(txtCod.getText());
                model = modelProduto.findByCod(cod);
                tblProduto.setModel(model);
                this.repaint();
            }
        }
             
    }//GEN-LAST:event_btnPesquisaActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linha = tblProduto.getSelectedRow();
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione um produto", "Erro - Produto não selecionado", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            Produto p = (Produto) tblProduto.getValueAt(linha, 1);
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir "+p+"?", "Excluir Cliente", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
            //Usuário clicou em Sim. Executar o código correspondente.
                ProdutoControle.delete(p);
            }
            Atualizar();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

