    public void cidade(String cpf){
        ClienteDao cd = new ClienteDao();
        Cliente c = cd.findByCpf(cpf);
        cboUf.setSelectedItem(c.getCidadeCliente().getEstado());
        cboCidade.setSelectedItem(c.getCidadeCliente());
    }
    public static void mostrar(Cliente c) {
        CadastroCliente dialog = CadastroCliente.getInstance(
                                new javax.swing.JFrame(), true);
        dialog.prepararMostrar(c);
        dialog.setVisible(true);
        tela =null;
        
    }
    
    static boolean cancel = false;
    public static Cliente editar(Cliente c) {
        CadastroCliente dialog = CadastroCliente.getInstance(
                                new javax.swing.JFrame(), true);
        dialog.prepararEditar(c);
        dialog.setVisible(true);
        //Gambiarra
        if(CadastroCliente.cancel == true){
            cancel = false;
            tela =null;
            return c;
        }
        else{
            c = dialog.getEstadoTela();
            tela = null;
            return c;
        }
    }
    public static Cliente inserir() {
        CadastroCliente dialog = CadastroCliente.getInstance(
                                new javax.swing.JFrame(), true);
        
        dialog.setVisible(true);
        //Gambiarra
        if(CadastroCliente.cancel == true)
            return null;
        else
            return dialog.getEstadoTela();

    }
    
    public Cliente getEstadoTela(){
        Cliente c = new Cliente();
        c.setNmCliente(txtNome.getText());
        c.setCpfCliente(txtCpf.getText());
        c.setRg(txtRg.getText());
        c.setDtNascCliente(txtDtNasc.getText());
        
        //Confere o Sexo selecionado na combo box e tranforma em char
        char sexo = 'N';
        if(cboSexo.getSelectedItem().equals("Masculino")){
            sexo = 'M';
        }
        else if(cboSexo.getSelectedItem().equals("Feminino")){
            sexo = 'F';
        }
        
        c.setSexoCliente(sexo);
        c.setCelularCliente(txtCelular.getText());
        c.setTelefoneCliente(txtTelefone.getText());
        c.setEmailCliente(txtEmail.getText());
        c.setCepCliente(txtCep.getText());
        c.setCidadeCliente((Cidade)cboCidade.getSelectedItem()); //Lembrando que ele envia o Objeto cidade, não só o nome
        c.setEndereco(txtEndereco.getText());
        return c;
     }
    
    public void prepararMostrar(Cliente c){
        //Dando Enable para todos componentes pois só vou mostar imformações nessa parte
        cliente = c;
        txtNome.setEnabled(false);
        txtCpf.setEnabled(false);
        txtRg.setEnabled(false);
        txtDtNasc.setEnabled(false);
        cboSexo.setEnabled(false);
        txtCelular.setEnabled(false);
        txtTelefone.setEnabled(false);
        txtEmail.setEnabled(false);
        txtCep.setEnabled(false);
        cboCidade.setEnabled(false);
        cboUf.setEnabled(false);
        txtEndereco.setEnabled(false);
        btnSalvar.setVisible(false);
        btnCancelar.setVisible(false);
        //Setando as informações recebidas por parâmetro
        
        txtNome.setText(c.getNmCliente());
        txtCpf.setText(c.getCpfCliente());
        txtRg.setText(c.getRg());
        txtDtNasc.setText(c.getDtNascCliente());
        
        if (c.getSexoCliente() == 'M'){
            cboSexo.setSelectedItem("Masculino");
        }else if (c.getSexoCliente() == 'F'){
            cboSexo.setSelectedItem("Feminino");
        }
            
        txtCelular.setText(c.getCelularCliente());
        txtTelefone.setText(c.getTelefoneCliente());
        txtEmail.setText(c.getEmailCliente());
        txtCep.setText(c.getCepCliente());
        for (int i = 0; i < cboUf.getItemCount(); i++) {
           Estado uf = (Estado)cboUf.getItemAt(i);
           if(uf.getCodEstado() == c.getCidadeCliente().getEstado().getCodEstado()){
                cboUf.setSelectedItem(cboUf.getItemAt(i));
                break;
           }
               
        }
        cboCidade.removeAllItems();
        CidadeDao cd = new CidadeDao();
        ArrayList cidadeList = cd.findByEstado(c.getCidadeCliente().getEstado());
        for (int i = 0; i <cidadeList.size(); i++) {
           cboCidade.addItem(cidadeList.get(i));
        }
        for (int i = 0; i < cboCidade.getItemCount(); i++) {
           Cidade cidade = (Cidade)cboCidade.getItemAt(i);
           if(cidade.getCdCidade() == c.getCidadeCliente().getCdCidade()){
                cboCidade.setSelectedItem(cboCidade.getItemAt(i));
                break;
           }
        }
        txtEndereco.setText(c.getEndereco());
        
    }
    
