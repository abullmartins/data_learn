    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        boolean vazio = false;
        boolean cpfvalido = Cliente.validaCpf(txtCpf.getText());
        if(txtNome.getText().equals("")){
             CadastroCliente.campoObrigatorio(txtNome);
             vazio = true;
        }
        if(txtCpf.getText().equals("   .   .   -  ")){
            CadastroCliente.campoObrigatorio(txtCpf);
            vazio = true;
        }else{
            if(!cpfvalido){
            campoObrigatorio(txtCpf);
            JOptionPane.showMessageDialog(null, "CPF Inválido", "Erro - Dados Inválidos", JOptionPane.ERROR_MESSAGE);
            vazio = true;
            }
        }
        if(txtRg.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtRg);
            vazio = true;
        }
        if(txtDtNasc.getText().equals("__/__/____")){
            CadastroCliente.campoObrigatorio(txtDtNasc);
            vazio = true;
        }
        if(txtCelular.getText().equals("(  )     -    ")){
            CadastroCliente.campoObrigatorio(txtCelular);
            vazio = true;
        }
        if(txtTelefone.getText().equals("(  )    -    ")){
            CadastroCliente.campoObrigatorio(txtTelefone);
            vazio = true;
        }
        if(txtEmail.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtEmail);
            vazio = true;
        }
        if(txtCep.getText().equals("__.___-___")){
            CadastroCliente.campoObrigatorio(txtCep);
            vazio = true;
        }
        if(txtEndereco.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtEndereco);
            vazio = true;
        }
        if(vazio == false){
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Existem campos vazios", "Erro - Campo Vazio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

