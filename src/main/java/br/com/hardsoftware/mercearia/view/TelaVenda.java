    private void btnCancelarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarItemActionPerformed
        // TODO add your handling code here:
        Funcionario f = LoginControle.getFuncOnline();
        if(f.getCargoFuncionario().equals("Administrador") || f.getCargoFuncionario().equals("Super Operador") || f.getCargoFuncionario().equals("Gerente")){
            int linha = tabelaProduto.getSelectedRow();
            int qtd = Integer.parseInt(txtQtd.getText());
            DefaultTableModel novoModel = modeloProduto.removeProduto(linha, model);
            tabelaProduto.setModel(novoModel);
            model = novoModel;
            AtualizarCompra();
        }
        else{
            Funcionario master = TelaLoginSuper.logar();
            if(master != null){
                int linha = tabelaProduto.getSelectedRow();
                int qtd = Integer.parseInt(txtQtd.getText());
                DefaultTableModel novoModel = modeloProduto.removeProduto(linha, model);
                tabelaProduto.setModel(novoModel);
                model = novoModel;
                AtualizarCompra();
            }
        }
    }//GEN-LAST:event_btnCancelarItemActionPerformed

