    public static ArrayList<Produto> getProdutos(){
        ArrayList<Produto> lista = new ArrayList();
        int i = tela.tabelaProduto.getRowCount();
        for (int j = 0; j < i; j++) {
            Produto p = (Produto)tela.tabelaProduto.getValueAt(j, 1);
            String sPreco = tela.tabelaProduto.getValueAt(j, 4).toString();
            double preco = Double.parseDouble(sPreco);
            p.setPrecoProduto(preco);
            lista.add(p);
        }
        return lista;
    }
