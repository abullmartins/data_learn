    private void txtCodBarraKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodBarraKeyReleased
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            selecionarProduto(txtCodBarra.getText());
            txtCodBarra.setText("");
        }
        else{
            ProdutoDao pd = new ProdutoDao();
            Produto p = pd.findByCodBarras(txtCodBarra.getText());
            txtPreco.setText(p.getPrecoProduto()+"");
            double precoTotal = Integer.parseInt(txtQtd.getText()) * p.getPrecoProduto();
            txtPrecoTotal.setText(String.valueOf(precoTotal));
            txtNomeProduto.setText(p.getNmProduto());
            txtQtd.setText("1");
            
        }
    }//GEN-LAST:event_txtCodBarraKeyReleased

