    public static Produto selecionar() {
       ConsultarProduto dialog = ConsultarProduto.getInstance(
                                new javax.swing.JFrame(), true);
        dialog.setVisible(true);
        Produto p = dialog.getProdutoSelecionado();   
        tela = null;
        if(!cancel){
            return p;
        }
        else    
            return null;

    }
    private Produto getProdutoSelecionado(){
        Produto pro;
        pro = (Produto) tblProduto.getValueAt(tblProduto.getSelectedRow(), 1);
        return pro;
    }
