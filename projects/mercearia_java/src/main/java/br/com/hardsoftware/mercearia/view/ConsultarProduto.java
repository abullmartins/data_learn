    private void txtNomeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if(cboCategoria.getSelectedItem().equals("Todos Produtos")){

                model = modelProduto.findByName(txtNome.getText());
                tblProduto.setModel(model);
                this.repaint();
            }else{
                model = modelProduto.findByNameCategoria(txtNome.getText(), (Categoria) cboCategoria.getSelectedItem());
                tblProduto.setModel(model);
                this.repaint();
            }
        }

    }//GEN-LAST:event_txtNomeKeyPressed

    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed

        if(cboCategoria.getSelectedItem().equals("Todos Produtos")){
            model = modelProduto.findByName(txtNome.getText());
            tblProduto.setModel(model);
        }
        else{
            model = modelProduto.findByNameCategoria(txtNome.getText(), (Categoria) cboCategoria.getSelectedItem());
            tblProduto.setModel(model);
            this.repaint();
        }

    }//GEN-LAST:event_btnPesquisaActionPerformed

    private void cboCategoriaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCategoriaItemStateChanged
        // TODO add your handling code here:
        if(cboCategoria.getSelectedItem().equals("Todos Produtos")){
            model = modelProduto.findAll();
            tblProduto.setModel(model);
            this.repaint();
        }else{
            model = modelProduto.findByCategoria((Categoria) cboCategoria.getSelectedItem());
            tblProduto.setModel(model);
            this.repaint();
        }
    }//GEN-LAST:event_cboCategoriaItemStateChanged

