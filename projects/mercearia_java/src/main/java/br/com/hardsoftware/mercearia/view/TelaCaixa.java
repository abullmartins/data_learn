    private void btnFecharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFecharActionPerformed
        // TODO add your handling code here:
        ArrayList<Venda> v = new ArrayList();
        for (int i = 0; i < tabelaVenda.getRowCount(); i++) {
            Venda venda = (Venda) tabelaVenda.getValueAt(i, 0);
            v.add(venda);
        }
        CaixaControle.fecharCaixa(v);
        modelVenda = modeloVenda.limpar(modelVenda);
        tabelaVenda.setModel(modelVenda);
        modelCaixa = modeloCaixa.findAll();
        tabelaCaixa.setModel(modelCaixa);
        btnIniciarVenda.setEnabled(false);
        btnAbrir.setEnabled(true);
        btnFechar.setEnabled(false);
        btnBloquear.setEnabled(false);
        this.repaint();
    }//GEN-LAST:event_btnFecharActionPerformed

    private void btnAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirActionPerformed
        // TODO add your handling code here:
        Funcionario f = LoginControle.getFuncOnline();
        Calendar c  = CaixaControle.abrirCaixa(f);
        modelCaixa = modeloCaixa.findAllAndNow(c, f);
        tabelaCaixa.setModel(modelCaixa);
        modelVenda = modeloVenda.limpar(modelVenda);
        tabelaVenda.setModel(modelVenda);
        btnAbrir.setEnabled(false);
        btnBloquear.setEnabled(true);
        btnFechar.setEnabled(true);
        tabelaVenda.setEnabled(true);
        tabelaCaixa.setEnabled(true);
        btnIniciarVenda.setEnabled(true);
        this.repaint();
    }//GEN-LAST:event_btnAbrirActionPerformed

