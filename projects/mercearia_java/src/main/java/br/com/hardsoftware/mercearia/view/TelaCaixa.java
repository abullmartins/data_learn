    private void atualizarTotal() {
        double soma = 0;
        for (int i = 0; i < tabelaVenda.getRowCount(); i++) {
            soma += Double.valueOf(tabelaVenda.getValueAt(i, 2).toString());
        }
        
        txtTotal.setText("R$ " + soma);
    }
    
    public static void addVenda(Venda v){
        tela.modelVenda = tela.modeloVenda.addVenda(v, tela.modelVenda);
        tela.tabelaVenda.setModel(tela.modelVenda);
        tela.atualizarTotal();
        tela.repaint();
    }

    
