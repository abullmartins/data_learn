    private void txtSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSenhaKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) { 
            f = LoginControle.logarSuper(txtUsuario.getText(), String.valueOf(txtSenha.getPassword()));
            if(f != null){
                this.dispose();
            }
            else
                JOptionPane.showMessageDialog(null, "Usuário ou senha estão incorretos!", "ERRO - Falha no Login", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtSenhaKeyPressed

