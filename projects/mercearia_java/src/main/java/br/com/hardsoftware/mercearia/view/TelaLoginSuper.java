    public static Funcionario logar() {
        TelaLoginSuper dialog = TelaLoginSuper.getInstance(
                new javax.swing.JFrame(), true);
        dialog.setVisible(true);
        //Gambiarra
        if (TelaLoginSuper.cancel) {
            cancel = false;
            tela = null;
            return f;
        } else {
            tela = null;
            return f;
        }
    }
    
