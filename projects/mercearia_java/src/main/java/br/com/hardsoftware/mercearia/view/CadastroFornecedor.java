    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //this.dispose();
        boolean vazio = false;
        boolean cnpjvalido = Fornecedor.validaCnpj(txtCnpj.getText());
        if(txtNome.getText().equals("")){
             CadastroFornecedor.campoObrigatorio(txtNome);
             vazio = true;
        }
        if(txtCnpj.getText().equals("  .   .   /    -  ")){
            CadastroFornecedor.campoObrigatorio(txtCnpj);
            vazio = true;
        }else{
            if(!cnpjvalido){
            campoObrigatorio(txtCnpj);
            JOptionPane.showMessageDialog(null, "CNPJ Inválido", "Erro - Dados Inválidos", JOptionPane.ERROR_MESSAGE);
            vazio = true;
            }
        }
        if(txtCelular.getText().equals("(  )     -    ")){
            CadastroFornecedor.campoObrigatorio(txtCelular);
            vazio = true;
        }
        if(txtTelefone.getText().equals("(  )    -    ")){
            CadastroFornecedor.campoObrigatorio(txtTelefone);
            vazio = true;
        }
        if(txtEmail.getText().equals("")){
            CadastroFornecedor.campoObrigatorio(txtEmail);
            vazio = true;
        }
        if(txtCep.getText().equals("__.___-___")){
            CadastroFornecedor.campoObrigatorio(txtCep);
            vazio = true;
        }
        if(txtEndereco.getText().equals("")){
            CadastroFornecedor.campoObrigatorio(txtEndereco);
            vazio = true;
        }
        if(vazio == false){
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Existem campos vazios", "Erro - Campo Vazio", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

