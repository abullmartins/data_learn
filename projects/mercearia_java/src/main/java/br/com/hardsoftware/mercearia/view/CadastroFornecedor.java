    public static void mostrar(Fornecedor f) {
        CadastroFornecedor dialog = CadastroFornecedor.getInstance(
                new javax.swing.JFrame(), true);
        dialog.prepararMostrar(f);
        dialog.setVisible(true);
        tela = null;

    }

    private static boolean cancel = false;

    public static Fornecedor editar(Fornecedor f) {
        CadastroFornecedor dialog = CadastroFornecedor.getInstance(
                new javax.swing.JFrame(), true);
        dialog.prepararEditar(f);
        dialog.setVisible(true);
        //Gambiarra
        if (CadastroFornecedor.cancel == true) {
            cancel = false;
            tela = null;
            return f;
        } else {
            f = dialog.getEstadoTela();
            tela = null;
            return f;
        }
    }
    
    public void cidade(String cnpj){
        FornecedorDao fu = new FornecedorDao();
        Fornecedor f = fu.findByCpf(cnpj);
        cbUf.setSelectedItem(f.getCidadeFornecedor().getEstado());
        cbCidade.setSelectedItem(f.getCidadeFornecedor());
    }

    public static Fornecedor inserir() {
        CadastroFornecedor dialog = CadastroFornecedor.getInstance(
                new javax.swing.JFrame(), true);

        dialog.setVisible(true);
        //Gambiarra
        if (CadastroFornecedor.cancel == true) {
            return null;
        } else {
            return dialog.getEstadoTela();
        }

    }

    public Fornecedor getEstadoTela() {
        Fornecedor f = new Fornecedor();
        f.setNmFornecedor(txtNome.getText());
        f.setCnpjFornecedor(txtCnpj.getText());
        f.setCelularFornecedor(txtCelular.getText());
        f.setTelefoneFornecedor(txtTelefone.getText());
        f.setObservFornecedor(txtAreaObserva.getText());
        boolean entrega = false;
        if (cbEntrega.getSelectedItem().equals("Não")) {
            entrega = false;
        } else if (cbEntrega.getSelectedItem().equals("Sim")) {
            entrega = true;
        }
        f.setEntregaFornecedor(entrega);
        f.setEmailFornecedor(txtEmail.getText());
        f.setCepFornecedor(txtCep.getText());
        f.setCidadeFornecedor((Cidade) cbCidade.getSelectedItem()); //Lembrando que ele envia o Objeto cidade, não só o nome
        f.setEnderecoFornecedor(txtEndereco.getText());
        return f;
    }

    public void prepararMostrar(Fornecedor f) {
        //Dando Enable para todos componentes pois só vou mostar imformações nessa parte
        txtNome.setEnabled(false);
        txtCnpj.setEnabled(false);
        txtCelular.setEnabled(false);
        txtTelefone.setEnabled(false);
        txtAreaObserva.setEnabled(false);
        cbEntrega.setEnabled(false);
        txtEmail.setEnabled(false);
        txtCep.setEnabled(false);
        cbCidade.setEnabled(false);
        cbUf.setEnabled(false);
        txtEndereco.setEnabled(false);
        btnSalvar.setVisible(false);
        btnCancelar.setVisible(false);
        //Setando as informações recebidas por parâmetro
        txtNome.setText(f.getNmFornecedor());
        txtCnpj.setText(f.getCnpjFornecedor());
        txtCelular.setText(f.getCelularFornecedor());
        txtTelefone.setText(f.getTelefoneFornecedor());
        txtAreaObserva.setText(f.getObservFornecedor());
        if (f.getEnderecoFornecedor() == "Não") {
            cbEntrega.setSelectedItem(false);
        } else if (f.getEnderecoFornecedor() == "Sim") {
            cbEntrega.setSelectedItem(true);
        }
        txtEmail.setText(f.getEmailFornecedor());
        txtCep.setText(f.getCepFornecedor());
        for (int i = 0; i < cbUf.getItemCount(); i++) {
           Estado uf = (Estado)cbUf.getItemAt(i);
           if(uf.getCodEstado() == f.getCidadeFornecedor().getEstado().getCodEstado()){
                cbUf.setSelectedItem(cbUf.getItemAt(i));
                break;
           }
        }
        cbCidade.removeAllItems();
        CidadeDao cd = new CidadeDao();
        ArrayList cidadeList = cd.findByEstado(f.getCidadeFornecedor().getEstado());
        for (int i = 0; i <cidadeList.size(); i++) {
            cbCidade.addItem(cidadeList.get(i));
        }
        for (int i = 0; i < cbCidade.getItemCount(); i++) {
           Cidade cidade = (Cidade)cbCidade.getItemAt(i);
           if(cidade.getCdCidade() == f.getCidadeFornecedor().getCdCidade()){
                cbCidade.setSelectedItem(cbCidade.getItemAt(i));
                break;
           }
        }
        
        txtEndereco.setText(f.getEnderecoFornecedor());
    }

