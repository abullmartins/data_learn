    private  Produto getEstadoTela() {
        Produto p = new Produto();
        p.setUnidadeProduto((UnidadeProduto) cboUnidade.getSelectedItem());
        p.setCategoria((Categoria) cboCategoria.getSelectedItem());
        p.setCodigoBarraProduto(txtCod.getText());
        p.setDescricaoProduto(txtDescricao.getText());
        p.setMarcaProduto(txtMarca.getText());
        p.setNmProduto(txtNome.getText());
        p.setPrecoProduto(Double.parseDouble(txtPreco.getText().replace(",", ".")));
        p.setQtdProduto(Integer.parseInt(txtQuant.getText()));
        p.setValorProduto(Double.parseDouble(txtValor.getText().replace(",", ".")));
        return p;
    }

