    public static Produto inserir() {
        CadastroProduto dialog = CadastroProduto.getInstance(
                                new javax.swing.JFrame(), true);
        
        dialog.setVisible(true);
            if(CadastroCliente.cancel == true){
                tela = null;
                return null;
            }
            else{
                Produto pro = dialog.getEstadoTela();
                tela = null;
                return pro;
            }
    }
    
