    public void prepararMostrar(Produto p){
        cboCategoria.setSelectedItem(p.getCategoria());
        cboUnidade.setSelectedItem(p.getUnidadeProduto());
        txtCod.setText(p.getCodigoBarraProduto()+"");
        txtDescricao.setText(p.getDescricaoProduto());
        txtMarca.setText(p.getMarcaProduto());
        txtNome.setText(p.getNmProduto());
        txtPreco.setText(p.getPrecoProduto()+"");
        txtQuant.setText(p.getQtdProduto()+"");
        txtValor.setText(p.getValorProduto() + "");
        
        txtValor.setEnabled(false);
        txtQuant.setEnabled(false);
        txtPreco.setEnabled(false);
        txtNome.setEnabled(false);
        txtMarca.setEnabled(false);
        txtDescricao.setEnabled(false);
        txtCod.setEnabled(false);
        cboUnidade.setEnabled(false);
        cboCategoria.setEnabled(false);
        btnSalvar.setText("OK");
        btnCancelar.setVisible(false);
    }
    
