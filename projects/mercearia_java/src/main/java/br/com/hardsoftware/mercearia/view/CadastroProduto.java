    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        boolean vazio = false;
        if(txtNome.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtNome);
            vazio = true;
        }
        if(txtCod.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtCod);
            vazio = true;
        }
        if(txtMarca.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtMarca);
            vazio = true;
        }
        if(txtPreco.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtPreco);
            vazio = true;
        }
        if(txtQuant.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtQuant);
            vazio = true;
        }
        if(txtValor.getText().equals("")){
            CadastroCliente.campoObrigatorio(txtValor);
            vazio = true;
        }
        if(!vazio){
            this.dispose();
        }
        else{
             JOptionPane.showMessageDialog(null, "Existem campos vazios", "Erro - Campo Vazio", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

