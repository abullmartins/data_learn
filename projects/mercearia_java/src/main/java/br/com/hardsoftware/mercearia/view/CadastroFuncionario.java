    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        boolean vazio = false;
        boolean cpfvalido = Funcionario.validaCpf(txtCpf.getText());
        if(txtNome.getText().equals("")){
             CadastroFuncionario.campoObrigatorio(txtNome);
             vazio = true;
        }
        if(txtCpf.getText().equals("   .   .   -  ")){
            CadastroFuncionario.campoObrigatorio(txtCpf);
            vazio = true;
        }else{
            if(!cpfvalido){
            campoObrigatorio(txtCpf);
            JOptionPane.showMessageDialog(null, "CPF Inválido", "Erro - Dados Inválidos", JOptionPane.ERROR_MESSAGE);
            vazio = true;
            }
        }
        if(txtRg.getText().equals("")){
            CadastroFuncionario.campoObrigatorio(txtRg);
            vazio = true;
        }
        if(txtDtNasc.getText().equals("__/__/____")){
            CadastroFuncionario.campoObrigatorio(txtDtNasc);
            vazio = true;
        }
        if(txtCelular.getText().equals("(  )     -    ")){
            CadastroFuncionario.campoObrigatorio(txtCelular);
            vazio = true;
        }
        if(txtTelefone.getText().equals("(  )    -    ")){
            CadastroFuncionario.campoObrigatorio(txtTelefone);
            vazio = true;
        }
        if(txtEmail.getText().equals("")){
            CadastroFuncionario.campoObrigatorio(txtEmail);
            vazio = true;
        }
        if(txtCep.getText().equals("__.___-___")){
            CadastroFuncionario.campoObrigatorio(txtCep);
            vazio = true;
        }
        if(txtEndereco.getText().equals("")){
            CadastroFuncionario.campoObrigatorio(txtEndereco);
            vazio = true;
        }
        if(vazio == false){
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Existem campos vazios", "Erro - Campo Vazio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

