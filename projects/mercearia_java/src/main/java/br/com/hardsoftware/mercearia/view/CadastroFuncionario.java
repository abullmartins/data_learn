    public Funcionario getEstadoTela() {
        Funcionario f = new Funcionario();
        f.setNmFuncionario(txtNome.getText());
        f.setCpfFuncionario(txtCpf.getText());
        f.setRgFuncionario(txtRg.getText());
        f.setDtNascFuncionario(txtDtNasc.getText());
        //Confere o Sexo selecionado na combo box e tranforma em char
        char sexo = 'N';
        if (cbSexo.getSelectedItem().equals("Masculino")) {
            sexo = 'M';
        } else if (cbSexo.getSelectedItem().equals("Feminino")) {
            sexo = 'F';
        }
        f.setSexoFuncionario(sexo);
        f.setCelularFuncionario(txtCelular.getText());
        f.setTelefoneFuncionario(txtTelefone.getText());
        f.setEmailFuncionario(txtEmail.getText());
        f.setCepFuncionario(txtCep.getText());
        f.setCidadeFuncionario((Cidade) cbCidade.getSelectedItem()); //Lembrando que ele envia o Objeto cidade, não só o nome
        f.setEnderecoFuncionario(txtEndereco.getText());
        f.setSalarioFuncionario(Double.parseDouble(txtSalario.getText()));
        f.setLoginFunconario(txtLogin.getText());
        f.setSenhaFuncionario(String.copyValueOf(txtSenha.getPassword()));
        f.setCargoFuncionario(cboCargo.getSelectedItem().toString());

        return f;
    }

    public void prepararMostrar(Funcionario f) {

        //Dando Enable para todos componentes pois só vou mostar imformações nessa parte
        txtNome.setEnabled(false);
        txtCpf.setEnabled(false);
        txtRg.setEnabled(false);
        txtDtNasc.setEnabled(false);
        cbSexo.setEnabled(false);
        txtCelular.setEnabled(false);
        txtTelefone.setEnabled(false);
        txtEmail.setEnabled(false);
        txtCep.setEnabled(false);
        cbCidade.setEnabled(false);
        cbUf.setEnabled(false);
        txtEndereco.setEnabled(false);
        txtSalario.setEnabled(false);
        txtLogin.setEnabled(false);
        txtSenha.setEnabled(false);
        cboCargo.setEnabled(false);
        btnSalvar.setVisible(false);
        btnCancelar.setVisible(false);

        //Setando as informações recebidas por parâmetro
        txtNome.setText(f.getNmFuncionario());
        txtCpf.setText(f.getCpfFuncionario());
        txtRg.setText(f.getRgFuncionario());
        txtDtNasc.setText(f.getDtNascFuncionario());

        if (f.getSexoFuncionario() == 'M') {
            cbSexo.setSelectedItem("Masculino");
        } else if (f.getSexoFuncionario() == 'F') {
            cbSexo.setSelectedItem("Feminino");
        }
        txtCelular.setText(f.getCelularFuncionario());
        txtTelefone.setText(f.getTelefoneFuncionario());
        txtEmail.setText(f.getEmailFuncionario());
        txtCep.setText(f.getCepFuncionario());
        for (int i = 0; i < cbUf.getItemCount(); i++) {
           Estado uf = (Estado)cbUf.getItemAt(i);
           if(uf.getCodEstado() == f.getCidadeFuncionario().getEstado().getCodEstado()){
                cbUf.setSelectedItem(cbUf.getItemAt(i));
                break;
           }
               
        }
        cbCidade.removeAllItems();
        CidadeDao cd = new CidadeDao();
        ArrayList cidadeList = cd.findByEstado(f.getCidadeFuncionario().getEstado());
        for (int i = 0; i <cidadeList.size(); i++) {
            cbCidade.addItem(cidadeList.get(i));
        }
        for (int i = 0; i < cbCidade.getItemCount(); i++) {
           Cidade cidade = (Cidade)cbCidade.getItemAt(i);
           if(cidade.getCdCidade() == f.getCidadeFuncionario().getCdCidade()){
                cbCidade.setSelectedItem(cbCidade.getItemAt(i));
                break;
           }
        }
        txtEndereco.setText(f.getEnderecoFuncionario());
        txtSalario.setText(String.valueOf(f.getSalarioFuncionario()));
        txtLogin.setText(f.getLoginFunconario());
        txtSenha.setText(f.getSenhaFuncionario());
        cboCargo.setSelectedItem(f.getCargoFuncionario());
    }

